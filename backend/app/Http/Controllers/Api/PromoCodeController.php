    /**
     * GET /admin/promo-codes
     * List all non-referral promo codes with filters.
     */
    public function index(Request $request)
    {
        $query = ReferralCode::with(['targetCustomer.user', 'createdBy'])
            ->whereNotIn('type', ['customer_referral']); // exclude personal referral codes

        // Search
        if ($request->filled('search')) {
            $s = $request->search;
            $query->where(function ($q) use ($s) {
                $q->where('code', 'like', "%{$s}%")
                  ->orWhere('name', 'like', "%{$s}%")
                  ->orWhere('description', 'like', "%{$s}%");
            });
        }

        // Filters
        if ($request->filled('type'))        $query->where('type', $request->type);
        if ($request->filled('event_type'))  $query->where('event_type', $request->event_type);
        if ($request->filled('status'))      $query->where('status', $request->status);
        if ($request->filled('reward_type')) $query->where('reward_type', $request->reward_type);
        if ($request->boolean('public'))     $query->where('is_public', true);
        if ($request->boolean('auto'))       $query->where('auto_generated', true);
        if ($request->boolean('expiring'))   $query->expiringSoon(7);

        // Target customer filter
        if ($request->filled('customer_id')) {
            $query->where('target_customer_id', $request->customer_id);
        }

        // Sort
        $allowed = ['name', 'code', 'type', 'event_type', 'status', 'times_used', 'total_revenue', 'created_at', 'valid_until'];
        $sortBy  = in_array($request->input('sort_by'), $allowed)
            ? $request->input('sort_by')
            : 'created_at';
        $query->orderBy($sortBy, $request->input('sort_order', 'desc'));

        $perPage = (int) $request->input('per_page', 20);
        return response()->json($query->paginate($perPage));
    }

