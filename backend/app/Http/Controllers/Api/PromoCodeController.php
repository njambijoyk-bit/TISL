    /**
     * POST /admin/promo-codes
     * Create a manual admin promo code (seasonal, flash sale, bulk, general).
     */
    public function store(Request $request)
    {
        $validator = Validator::make($request->all(), [
            'name'                      => 'required|string|max:255',
            'code'                      => 'nullable|string|max:50|unique:referral_codes,code',
            'description'               => 'nullable|string',
            'type'                      => 'required|in:general,first_time,bulk_order,vip,birthday,event,seasonal,flash_sale',
            'event_type'                => 'nullable|in:birthday,first_time,vip_upgrade,loyalty_milestone,win_back,seasonal,flash_sale,bulk_order,general',
            'target_customer_id'        => 'nullable|exists:customers,id',
            'reward_type'               => 'required|in:percentage,fixed_amount,free_shipping,store_credit',
            'reward_value'              => 'required|numeric|min:0',
            'max_uses'                  => 'nullable|integer|min:1',
            'max_uses_per_customer'     => 'nullable|integer|min:1',
            'valid_from'                => 'nullable|date',
            'valid_until'               => 'nullable|date|after:valid_from',
            'min_order_value'           => 'nullable|numeric|min:0',
            'min_items'                 => 'nullable|integer|min:1',
            'stackable'                 => 'boolean',
            'is_public'                 => 'boolean',
            'auto_apply'                => 'boolean',
            'applicable_categories'     => 'nullable|array',
            'applicable_products'       => 'nullable|array',
            'excluded_products'         => 'nullable|array',
            'applicable_customer_types' => 'nullable|array',
            'applicable_tiers'          => 'nullable|array',
            'promo_color'               => 'nullable|string|max:20',
            'display_tags'              => 'nullable|array',
            'admin_notes'               => 'nullable|string',
            'status'                    => 'nullable|in:draft,active,paused',
        ]);

        if ($validator->fails()) {
            return response()->json(['errors' => $validator->errors()], 422);
        }

        DB::beginTransaction();
        try {
            $code = ReferralCode::create([
                ...$request->only([
                    'name', 'description', 'type', 'event_type',
                    'target_customer_id', 'reward_type', 'reward_value',
                    'max_uses', 'max_uses_per_customer',
                    'valid_from', 'valid_until',
                    'min_order_value', 'min_items',
                    'stackable', 'is_public', 'auto_apply',
                    'applicable_categories', 'applicable_products',
                    'excluded_products', 'applicable_customer_types',
                    'applicable_tiers', 'promo_color', 'display_tags',
                    'admin_notes',
                ]),
                'code'                 => $request->code
                                            ? strtoupper($request->code)
                                            : ReferralCode::generateUniqueCode(10),
                'status'               => $request->input('status', 'active'),
                'auto_generated'       => false,
                'referrer_reward_type' => 'none',
                'referrer_reward_value'=> 0,
                'created_by'           => $request->user()->id,
                'updated_by'           => $request->user()->id,
            ]);

            DB::commit();

            $this->logPromoCreated($code);

            return response()->json([
                'message' => 'Promo code created successfully.',
                'code'    => $code->load(['targetCustomer.user', 'createdBy']),
            ], 201);

        } catch (\Exception $e) {
            DB::rollBack();
            Log::error('Promo code creation failed: ' . $e->getMessage());
            return response()->json(['message' => 'Failed to create promo code.'], 500);
        }
    }

