    /**
     * GET /admin/promo-codes/statistics
     */
    public function statistics()
    {
        $base = ReferralCode::whereNotIn('type', ['customer_referral']);

        $byEventType = (clone $base)
            ->selectRaw('event_type, COUNT(*) as count, SUM(times_used) as uses, SUM(total_revenue) as revenue')
            ->groupBy('event_type')
            ->get();

        $autoGenerated = (clone $base)->where('auto_generated', true)->count();
        $adminCreated  = (clone $base)->where('auto_generated', false)->count();

        return response()->json([
            'counts' => [
                'total'         => (clone $base)->count(),
                'active'        => (clone $base)->where('status', 'active')->count(),
                'expired'       => (clone $base)->where('status', 'expired')->count(),
                'depleted'      => (clone $base)->where('status', 'depleted')->count(),
                'auto_generated'=> $autoGenerated,
                'admin_created' => $adminCreated,
                'expiring_soon' => (clone $base)->expiringSoon(7)->count(),
            ],
            'totals' => [
                'revenue'        => (clone $base)->sum('total_revenue'),
                'discount_given' => (clone $base)->sum('total_discount_given'),
                'total_uses'     => (clone $base)->sum('times_used'),
                'orders'          => (clone $base)->sum('total_orders'),  
                'avg_order_value' => (clone $base)->avg('average_order_value'), 
            ],
            'by_event_type'  => $byEventType,
            'top_by_revenue' => (clone $base)->orderBy('total_revenue', 'desc')->limit(5)
                ->get(['id', 'name', 'code', 'event_type', 'total_revenue', 'times_used']),
        ]);
    }

