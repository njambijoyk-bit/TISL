    /**
     * POST /customer/promo-codes/validate
     * Validate a promo code at checkout.
     * Returns discount amount preview without applying it.
     */
    public function validateCode(Request $request)
    {
        $request->validate([
            'code'                => 'required|string',
            'order_value'         => 'required|numeric|min:0',
            'referral_discount'   => 'nullable|numeric|min:0',
            'exchange_rate_to_kes'=> 'nullable|numeric|min:0.0001', // ← add
        ]);

        $customer = $request->user()->customer;
        if (!$customer) {
            return response()->json(['valid' => false, 'message' => 'Customer profile not found.'], 404);
        }

        $result = $this->service->validatePromoCode(
            code:             $request->code,
            customer:         $customer,
            orderValue:       (float) $request->order_value,
            referralDiscount: (float) ($request->referral_discount ?? 0),
            exchangeRateToKes:(float) ($request->exchange_rate_to_kes ?? 1.0), // ← add
        );

        if (!$result['valid']) {
            return response()->json($result, 422);
        }

        return response()->json([
            'valid'        => true,
            'message'      => $result['message'],
            'code'         => $result['code']->code,
            'name'         => $result['code']->name,
            'description'  => $result['code']->description,
            'reward_type'  => $result['code']->reward_type,
            'reward_value' => $result['code']->reward_value,
            'discount'     => $result['discount'],
            'stackable'    => $result['code']->stackable,
        ]);
    }

